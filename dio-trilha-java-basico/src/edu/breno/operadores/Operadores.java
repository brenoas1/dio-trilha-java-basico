package edu.breno.operadores;

public class Operadores {
    public static void main(String[] args) {
        int numero = 5;

        numero = - numero;

        System.out.println(numero);

        numero = numero * -1;
        //para tornar um numero positivo novamente o numero deve ser multiplicado por -1

        System.out.println(numero);
    }
}
